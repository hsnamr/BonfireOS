void gui_draw_apply_palette(void)
{
    uint8_t full[256 * 3];
    for (int i = 0; i < 256 * 3; i++)
        full[i] = 0;
    for (int i = 0; i < 16 * 3; i++)
        full[i] = gui_palette[i];
    video_mode13_set_palette(full);
}

void gui_draw_fill_rect(uint8_t *fb, int x0, int y0, int w, int h, uint8_t color)
{
    if (x0 < 0) { w += x0; x0 = 0; }
    if (y0 < 0) { h += y0; y0 = 0; }
    if (x0 + w > GUI_FB_W) w = GUI_FB_W - x0;
    if (y0 + h > GUI_FB_H) h = GUI_FB_H - y0;
    if (w <= 0 || h <= 0) return;
    for (int y = 0; y < h; y++) {
        uint8_t *row = fb + (y0 + y) * GUI_FB_W + x0;
        for (int x = 0; x < w; x++)
            row[x] = color;
    }
}

void gui_draw_char(uint8_t *fb, int x, int y, char c, uint8_t fg, uint8_t bg)
{
    const uint8_t *glyph = FONT_ASCII[0];
    unsigned char u = (unsigned char)c;
    if (u >= 32 && u <= 126)
        glyph = FONT_ASCII[u - 32];
    for (int row = 0; row < 8; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < 8; col++) {
            uint8_t color = (bits & 0x80) ? fg : bg;
            int px = x + col;
            int py = y + row;
            if (px >= 0 && px < GUI_FB_W && py >= 0 && py < GUI_FB_H)
                fb[py * GUI_FB_W + px] = color;
            bits <<= 1;
        }
    }
}

void gui_draw_text(uint8_t *fb, int x, int y, const char *s, uint8_t fg, uint8_t bg)
{
    while (*s) {
        gui_draw_char(fb, x, y, *s++, fg, bg);
        x += 8;
    }
}

void gui_draw_text_len(uint8_t *fb, int x, int y, const char *s, int len, uint8_t fg, uint8_t bg)
{
    for (int i = 0; i < len && s[i]; i++) {
        gui_draw_char(fb, x, y, s[i], fg, bg);
        x += 8;
    }
}

void gui_draw_window(uint8_t *fb, int x, int y, int w, int h, const char *title, bool active)
{
    uint8_t frame_hi = active ? GUI_C_WHITE : GUI_C_LTGRAY;
    uint8_t frame_lo = GUI_C_DKGRAY;
    uint8_t title_bg = active ? GUI_C_BR_BLUE : GUI_C_DKGRAY;
    uint8_t title_fg = GUI_C_WHITE;

    gui_draw_fill_rect(fb, x + 2, y + 2, w, h, GUI_C_BLACK);
    gui_draw_fill_rect(fb, x, y, w, h, GUI_C_LTGRAY);
    gui_draw_fill_rect(fb, x, y, w, 2, frame_hi);
    gui_draw_fill_rect(fb, x, y, 2, h, frame_hi);
    gui_draw_fill_rect(fb, x, y + h - 2, w, 2, frame_lo);
    gui_draw_fill_rect(fb, x + w - 2, y, 2, h, frame_lo);
    gui_draw_fill_rect(fb, x + 2, y + 2, w - 4, 12, title_bg);
    gui_draw_text(fb, x + 4, y + 3, title, title_fg, title_bg);
    gui_draw_fill_rect(fb, x + 2, y + 14, w - 4, h - 16, GUI_C_LTGRAY);
}

void gui_draw_button(uint8_t *fb, int x, int y, int w, int h, const char *label, bool pressed, bool highlight)
{
    uint8_t hi = pressed ? GUI_C_DKGRAY : GUI_C_WHITE;
    uint8_t lo = pressed ? GUI_C_WHITE : GUI_C_DKGRAY;
    uint8_t fill = highlight ? GUI_C_BR_CYAN : GUI_C_LTGRAY;
    gui_draw_fill_rect(fb, x, y, w, h, fill);
    gui_draw_fill_rect(fb, x, y, w, 1, hi);
    gui_draw_fill_rect(fb, x, y, 1, h, hi);
    gui_draw_fill_rect(fb, x, y + h - 1, w, 1, lo);
    gui_draw_fill_rect(fb, x + w - 1, y, 1, h, lo);
    int len = 0;
    while (label[len]) len++;
    int lx = x + (w - len * 8) / 2;
    int ly = y + (h - 8) / 2;
    gui_draw_text(fb, lx, ly, label, GUI_C_BLACK, fill);
}

bool gui_draw_hit(int px, int py, int x, int y, int w, int h)
{
    return px >= x && px < x + w && py >= y && py < y + h;
}
